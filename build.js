    fs.writeFileSync(htmlPath, outputHtml, 'utf8');

    const srcSize = (fs.statSync(backupPath).size / 1024).toFixed(0);
    const outSize = (fs.statSync(htmlPath).size / 1024).toFixed(0);
    console.log(`\n📊 결과:`);
